// Generate the double-clickable <productName>.app that runs the app FROM SOURCE
// (the dev path: clone → double-click → live window; git pull → relaunch →
// updated). Run: `npm run make:launcher` / `make launcher`.
//
// Generated, not hand-written, so identity can't drift from package.json — the
// OneProduction convention (scripts/make-launcher.mjs there). The .app is a
// stub: its executable backgrounds desktop/launch.sh and exits, leaving only the
// Electron window.
//
// NOTE this is the *dev* launcher and is unrelated to the shipped .dmg, which
// electron-builder produces and which contains a real Electron app.
import { chmodSync, copyFileSync, existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const desktop = resolve(here, '..');
const repo = resolve(desktop, '..');

const pkg = JSON.parse(readFileSync(resolve(repo, 'package.json'), 'utf8'));
const name = pkg.productName;
const id = `${pkg.appConfig.appIdNamespace}.${pkg.name}.launcher`;

const app = resolve(repo, `${name}.app`);
const macos = resolve(app, 'Contents/MacOS');
rmSync(app, { recursive: true, force: true });
mkdirSync(macos, { recursive: true });

// The stub. Logs go under the repo (logs/), so they travel with the folder and
// are easy to point a user at; the previous session is kept as launch.log.1 so
// launch.log is always just the current one.
const exe = resolve(macos, name);
writeFileSync(
  exe,
  `#!/bin/bash
# GENERATED by desktop/scripts/make-launcher.mjs — do not edit; run \`make launcher\`.
# Double-clickable launcher: a .app runs its executable directly (no Terminal
# window). Resolves the repo it lives in, hands off to desktop/launch.sh in the
# background, and exits so only the Electron window remains.
DIR="$(cd "$(dirname "$0")/../../.." && pwd)" # repo root: ${name}.app/Contents/MacOS -> ../../..

LOG_DIR="$DIR/logs"
mkdir -p "$LOG_DIR"
LOG="$LOG_DIR/launch.log"
[ -f "$LOG" ] && mv -f "$LOG" "$LOG.1"
echo "===== ${name} launch $(date '+%Y-%m-%d %H:%M:%S %z') =====" >"$LOG"
nohup "$DIR/desktop/launch.sh" >>"$LOG" 2>&1 &
exit 0
`,
);
chmodSync(exe, 0o755);

// Icon, from the same master the packaged app and dock use.
let iconLine = '';
const icns = resolve(desktop, 'build-resources/icon.icns');
if (existsSync(icns)) {
  const resources = resolve(app, 'Contents/Resources');
  mkdirSync(resources, { recursive: true });
  copyFileSync(icns, resolve(resources, 'icon.icns'));
  iconLine = '\n  <key>CFBundleIconFile</key><string>icon</string>';
}

writeFileSync(
  resolve(app, 'Contents/Info.plist'),
  `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
  <key>CFBundleExecutable</key><string>${name}</string>${iconLine}
  <key>CFBundleIdentifier</key><string>${id}</string>
  <key>CFBundleName</key><string>${name}</string>
  <key>CFBundleDisplayName</key><string>${name}</string>
  <key>CFBundlePackageType</key><string>APPL</string>
  <key>CFBundleInfoDictionaryVersion</key><string>6.0</string>
  <key>CFBundleShortVersionString</key><string>${pkg.version}</string>
  <key>CFBundleVersion</key><string>1</string>
  <key>LSMinimumSystemVersion</key><string>10.15</string>
  <key>NSHighResolutionCapable</key><true/>
</dict>
</plist>
`,
);
writeFileSync(resolve(app, 'Contents/PkgInfo'), 'APPL????');

console.log(`Generated ${name}.app (${id}) → double-click to run from source.`);
